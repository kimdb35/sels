package d190612;


import java.util.Scanner;

// 1247 암호 해독 문제
public class Solution {

    static public void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        int T = sc.nextInt();

        for(int testCase=1; testCase<=T; testCase++) {
            int ans = 0;
            System.out.println("#"+testCase+" "+ans);
        }
        sc.close();
    }
}
